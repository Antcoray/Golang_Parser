package main

import "fmt"

func main() {
	fmt.Println("Демонстрация всех операторов присваивания в Go")
	fmt.Println("==============================================")

	// 1. Простое присваивание (=)
	fmt.Println("\n1. Простое присваивание (=):")
	var a int
	a = 10
	fmt.Printf("a = 10 → a = %d\n", a)

	// 2. Короткое объявление (:=)
	fmt.Println("\n2. Короткое объявление (:=):")
	b := 5
	fmt.Printf("b := 5 → b = %d\n", b)

	// 3. Сложение с присваиванием (+=)
	fmt.Println("\n3. Сложение с присваиванием (+=):")
	c := 7
	c += 3 // c = c + 3
	fmt.Printf("c := 7; c += 3 → c = %d\n", c)

	// 4. Вычитание с присваиванием (-=)
	fmt.Println("\n4. Вычитание с присваиванием (-=):")
	d := 15
	d -= 4 // d = d - 4
	fmt.Printf("d := 15; d -= 4 → d = %d\n", d)

	// 5. Умножение с присваиванием (*=)
	fmt.Println("\n5. Умножение с присваиванием (*=):")
	e := 6
	e *= 3 // e = e * 3
	fmt.Printf("e := 6; e *= 3 → e = %d\n", e)

	// 6. Деление с присваиванием (/=)
	fmt.Println("\n6. Деление с присваиванием (/=):")
	f := 20
	f /= 4 // f = f / 4
	fmt.Printf("f := 20; f /= 4 → f = %d\n", f)

	// 7. Остаток от деления с присваиванием (%=)
	fmt.Println("\n7. Остаток от деления с присваиванием (%%=):")
	g := 17
	g %= 5 // g = g % 5
	fmt.Printf("g := 17; g %%= 5 → g = %d\n", g)

	// 8. Побитовое И с присваиванием (&=)
	fmt.Println("\n8. Побитовое И с присваиванием (&=):")
	h := 12 // 1100 в двоичной
	h &= 10 // 1010 в двоичной (результат: 1000 = 8)
	fmt.Printf("h := 12 (1100); h &= 10 (1010) → h = %d (1000)\n", h)

	// 9. Побитовое ИЛИ с присваиванием (|=)
	fmt.Println("\n9. Побитовое ИЛИ с присваиванием (|=):")
	i := 12 // 1100 в двоичной
	i |= 10 // 1010 в двоичной (результат: 1110 = 14)
	fmt.Printf("i := 12 (1100); i |= 10 (1010) → i = %d (1110)\n", i)

	// 10. Побитовое исключающее ИЛИ с присваиванием (^=)
	fmt.Println("\n10. Побитовое исключающее ИЛИ с присваиванием (^=):")
	j := 12 // 1100 в двоичной
	j ^= 10 // 1010 в двоичной (результат: 0110 = 6)
	fmt.Printf("j := 12 (1100); j ^= 10 (1010) → j = %d (0110)\n", j)

	// 11. Сдвиг влево с присваиванием (<<=)
	fmt.Println("\n11. Сдвиг влево с присваиванием (<<=):")
	k := 3  // 0011 в двоичной
	k <<= 2 // сдвиг на 2 бита влево (результат: 1100 = 12)
	fmt.Printf("k := 3 (0011); k <<= 2 → k = %d (1100)\n", k)

	// 12. Сдвиг вправо с присваиванием (>>=)
	fmt.Println("\n12. Сдвиг вправо с присваиванием (>>=):")
	l := 16 // 10000 в двоичной
	l >>= 2 // сдвиг на 2 бита вправо (результат: 00100 = 4)
	fmt.Printf("l := 16 (10000); l >>= 2 → l = %d (00100)\n", l)

	// 13. Сброс бита с присваиванием (И НЕ) (&^=)
	fmt.Println("\n13. Сброс бита с присваиванием (И НЕ) (&^=):")
	m := 15 // 1111 в двоичной
	m &^= 6 // 0110 в двоичной (сбрасывает биты, установленные в 6; результат: 1001 = 9)
	fmt.Printf("m := 15 (1111); m &^= 6 (0110) → m = %d (1001)\n", m)

	fmt.Println("\n==============================================")
	fmt.Println("Все 13 операторов присваивания успешно продемонстрированы!")
}
